package day5;

public class D6_PlusSignInASqureSape {

	public static void main(String[] args) {
		int n=19;
		for(int i=0;i<n;i++) {
			if(i==9) {
				System.out.println(" ");
			}else {
			for(int j=0;j<n;j++) {
				if(j==8||j==9) {System.out.print(" ");
				}else {System.out.print("*  ");}
			}
			
			
			
			}
			
			System.out.println();
			
		}

	}

}
