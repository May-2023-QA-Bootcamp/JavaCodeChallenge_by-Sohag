package day5;

public class D5_RightAngleShape {

	public static void main(String[] args) {
		int n=10;
		for(int i=0;i<n;i++) { //outer loop
			
			//inner loop to print x then white space
			for(int k=0;k<=i;k++) {
				System.out.print("* ");
			}	
			
		System.out.println(); //it is in outerloop block--producing line
			
		}
	}

}
